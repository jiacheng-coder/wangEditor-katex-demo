<template>
  <div>
    <div>
      <button @click="insertText">insert text</button>
      <button @click="printHtml">print html</button>
      <button @click="disable">disable</button>
    </div>
    <div style="border: 1px solid #ccc; margin-top: 10px;">
      <Toolbar :editor="editorRef" :defaultConfig="toolbarConfig" :mode="mode" style="border-bottom: 1px solid #ccc" />
      <Editor :defaultConfig="editorConfig" :mode="mode" v-model="valueHtml" style="height: 650px; overflow-y: hidden;"
        @onCreated="handleCreated" @onChange="handleChange" @onDestroyed="handleDestroyed" @onFocus="handleFocus"
        @onBlur="handleBlur" @customAlert="customAlert" @customPaste="customPaste" />
    </div>
    <div style="margin-top: 10px;">
      <textarea v-model="valueHtml" readonly style="width: 100%; height: 200px; outline: none;"></textarea>
    </div>
  </div>
</template>

<script>
import "@wangeditor/editor/dist/css/style.css";
import { Editor, Toolbar } from '@wangeditor/editor-for-vue'

export default {
  components: { Editor, Toolbar },
  data() {
    return {
      editorRef: null,
      valueHtml: "<p>hello</p>",
      mode: "default", // Make sure to set the appropriate default value for 'mode'
      toolbarConfig: {
        insertKeys: {
          index: 0,
          keys: [
            "insertFormula", // “插入公式”菜单
            "kityFormula", // “编辑公式”菜单
            "myscriptMath", //"手写公式"菜单
          ],
        },
      },
      editorConfig: {
        placeholder: "请输入内容...",
        hoverbarKeys: {
          formula: {
            // menuKeys: ["kityFormula"], // “编辑公式”菜单
          },
        },
      },
    };
  },
  mounted() {
    setTimeout(() => {
      this.valueHtml = "<p>模拟 Ajax 异步设置内容</p>";
    }, 1500);
  },
  beforeUnmount() {
    const editor = this.editorRef;
    if (editor == null) return;

    editor.destroy();
  },
  methods: {
    handleCreated(editor) {
      console.log("created", editor);
      this.editorRef = editor;
    },
    handleChange(editor) {
      console.log("change:", editor.getHtml());
    },
    handleDestroyed(editor) {
      console.log("destroyed", editor);
    },
    handleFocus(editor) {
      console.log("focus", editor);
    },
    handleBlur(editor) {
      console.log("blur", editor);
    },
    customAlert(info, type) {
      alert(`【自定义提示】${type} - ${info}`);
    },
    customPaste(editor, event, callback) {
      console.log("ClipboardEvent 粘贴事件对象", event);

      // 自定义插入内容
      editor.insertText("xxx");

      // 返回值（注意，vue 事件的返回值，不能用 return）
      callback(false); // 返回 false ，阻止默认粘贴行为
      // callback(true) // 返回 true ，继续默认的粘贴行为
    },
    insertText() {
      const editor = this.editorRef;
      if (editor == null) return;
      editor.insertText("hello world");
    },
    printHtml() {
      const editor = this.editorRef;
      if (editor == null) return;
      console.log(editor.getHtml());
    },
    disable() {
      const editor = this.editorRef;
      if (editor == null) return;
      editor.disable();
    },
  },
};
</script>
